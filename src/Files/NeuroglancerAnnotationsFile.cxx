/**
 * Read the neuroglancer pins file.
 *
 * @param filenameIn
 *    Name of the data file.
 * @throws DataFileException
 *    If the file was not successfully read.
 */
void
NeuroglancerAnnotationsFile::readNeuroglancerFile(const AString& filenameIn)
{
   FileInformation fileInfo(filenameIn);
    const AString nameNoPath(fileInfo.getFileName());
    if (nameNoPath != "info") {
        throw DataFileException("Name of file MUST be named \"info\" but is named \""
                                + nameNoPath + "\"");
    }
    
    m_infoFileDirectoryName = fileInfo.getAbsolutePath();
    
//    const AString spatial0DirName(fileInfo.getAbsolutePath()
//                                  + QDir::separator()
//                                  + "spatial0");
    
    AString errorMessage;
        
    if ( ! errorMessage.isEmpty()) {
        throw DataFileException(errorMessage);
    }
    
    const AString filename(fileInfo.getAbsoluteFilePath());
    QFile jsonfile(filename);
    if ( ! jsonfile.open(QIODevice::ReadOnly)) {
        throw DataFileException("Error opening "
                                + filename
                                + ": "
                                + jsonfile.errorString());
    }
    
    const bool printJsonFlag(false);
    QByteArray dataRead(jsonfile.readAll());
    QString s(dataRead);
    if (printJsonFlag) {
        std::cout << "-->" << s << "<---" << std::endl;
    }
    QJsonParseError jsonError;
    QJsonDocument json = QJsonDocument::fromJson(dataRead,
                                                 &jsonError);
    if (jsonError.error != QJsonParseError::NoError) {
        throw DataFileException("Failed to read JSON from "
                                + filename
                                + " with error: "
                                + jsonError.errorString()
                                + " at offset: "
                                + QString::number(jsonError.offset));
    }
    
    const bool prettyPrintFlag(true);
    if (prettyPrintFlag) {
        AString nameOut(FileInformation::assembleFileComponents(fileInfo.getAbsolutePath(),
                                                                "formatted",
                                                                "json"));
        QByteArray jsonOut(json.toJson(QJsonDocument::JsonFormat::Indented));
        QFile fileOut(nameOut);
        if (fileOut.open(QIODevice::WriteOnly)) {
            fileOut.write(jsonOut);
            fileOut.close();
        }
        else {
            throw DataFileException("Error opening " + nameOut
                                    + ": " + fileOut.errorString());
        }
    }
    
    if ( ! json.isObject()) {
        throw DataFileException("Top level JSON in "
                                + filename
                                + " is not a JsonObject");
    }

    try {
        readNeuroglancerJson(fileInfo,
                                 json.object());
        
    }
    catch (const DataFileException& dfe) {
        throw DataFileException("While reading "
                                + filename
                                + ": "
                                + dfe.whatString());
    }
}

/**
 * Read the Neuroglancer JSON
 * @param topObject
 *    The top-level JSON object
 * @throw DataFileException
 */
void
NeuroglancerAnnotationsFile::readNeuroglancerJson(const FileInformation& fileInfo,
                                       const QJsonObject& root)
{
    AString errorMessage;
    
    const AString rootTypeName = root.value("@type").toString();
    if (rootTypeName != "neuroglancer_annotations_v1") {
        errorMessage.appendWithNewLine("@type="
                                       + rootTypeName
                                       + "but should be: neuroglancer_annotations_v1");
    }
    const QString annTypeName(root.value("annotation_type").toString().toUpper());
    if (annTypeName == "POINT") {
        m_annotationType = NeuroglancerAnnotationTypeEnum::POINT;
    }
    else if (annTypeName == "LINE") {
        m_annotationType = NeuroglancerAnnotationTypeEnum::LINE;
    }
    else if (annTypeName == "AXIS_ALIGNED_BOUNDING_BOX") {
        m_annotationType = NeuroglancerAnnotationTypeEnum::AXIS_ALIGNED_BOUNDING_BOX;
    }
    else if (annTypeName == "ELLIPSOID") {
        m_annotationType = NeuroglancerAnnotationTypeEnum::ELLIPSOID;
    }
    else if (annTypeName == "POLYLINE") {
        m_annotationType = NeuroglancerAnnotationTypeEnum::POLYLINE;
    }
    else {
        errorMessage.appendWithNewLine("Unrecognized 'annotation_type'="
                                       + annTypeName);
    }
    if (m_annotationType != NeuroglancerAnnotationTypeEnum::POINT) {
        errorMessage.appendWithNewLine("annotation_type="
                                       + annTypeName
                                       + " not supported.");
    }
    
    const AString byIdDirName(root.value("by_id").toObject().value("key").toString());
    if (byIdDirName.isEmpty()) {
        errorMessage.appendWithNewLine("by_id is missing or empty.");
    }
    else {
        m_byIdDirectoryName = (m_infoFileDirectoryName
                         + QDir::separator()
                         + byIdDirName);
        if ( ! QDir(byIdDirName).exists()) {
            errorMessage.appendWithNewLine("by_id directory does not exist: "
                                           + m_byIdDirectoryName);
        }
    }

    readDimensions(root.value("dimensions").toObject());
    
    m_lowerBound = readFloatArray(root.value("lower_bound").toArray());
    m_upperBound = readFloatArray(root.value("upper_bound").toArray());

    readProperties(root.value("properties").toArray());
    readSpatial(root.value("spatial").toArray());
}

