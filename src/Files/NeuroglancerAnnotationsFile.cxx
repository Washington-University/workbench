void
NeuroglancerAnnotationsFile::readDimensions(const QJsonObject &dimsObj)
{
    for (auto it = dimsObj.constBegin(); it != dimsObj.constEnd(); ++it) {
        const QJsonArray pair = it.value().toArray();
        const AString dimName(it.key().toLower());
        Axis axis(Axis::X);
        if (dimName == 'x') {
            axis = Axis::X;
        }
        else if (dimName == 'y') {
            axis = Axis::Y;
        }
        else if (dimName == 'z') {
            axis = Axis::Z;
        }
        else {
            throw DataFileException("Unrecogized dimension="
                                    + it.key());
        }
        
        float resolution(0.0);
        Unit unit(Unit::UNITLESS);
        if (pair.size() >= 1) {
            resolution = pair.at(0).toDouble();
        }
        float unitScale = 1.0;
        if (pair.size() >= 2) {
            const QString unitName(pair.at(1).toString().toLower());
            if (unitName == "m") {
                unit = Unit::MILLIMETERS;
                unitScale = 1000;
            }
            else if (unitName == "cm") {
                unit = Unit::MILLIMETERS;
                unitScale = 100;
            }
            else if (unitName == "mm") {
                unit = Unit::MILLIMETERS;
                unitScale = 1.0;
            }
            else if (unitName == "um") {
                unit = Unit::MILLIMETERS;
                unitScale = 0.001;
            }
            else if (unitName == "nm") {
                unit = Unit::MILLIMETERS;
                unitScale = 0.000001;
            }
            else if (unitName == "s") {
                unit = Unit::SECONDS;
            }
            else if (unitName.isEmpty()) {
                unit = Unit::UNITLESS;
            }
            else {
                throw DataFileException("Unsupported unit="
                                        + pair.at(1).toString());
            }
            
            switch (unit) {
                case Unit::MILLIMETERS:
                    break;
                case Unit::SECONDS:
                    throw DataFileException("Seconds not supported for unit");
                    break;
                case Unit::UNITLESS:
                    throw DataFileException("Unitless not supported for unit");
                    break;
            }
        }
        
        Dimension* d(NULL);
        switch (axis) {
            case Axis::X:
                d = &m_xDimension;
                d->m_axis = Axis::X;
                break;
            case Axis::Y:
                d = &m_yDimension;
                d->m_axis = Axis::Y;
                break;
            case Axis::Z:
                d = &m_zDimension;
                d->m_axis = Axis::Z;
                break;
        }
        d->m_units      = unit;
        d->m_unitScale  = unitScale;
        d->m_resolution = resolution;
        d->m_valid      = true;
    }
}

void
NeuroglancerAnnotationsFile::readProperties(const QJsonArray &propsArr)
{
    /*
     * m_lowerBound.size() == 3 for XYZ
     */
    int64_t offset(sizeof(float) * m_lowerBound.size());
    switch (m_annotationType) {
        case NeuroglancerAnnotationTypeEnum::INVALID:
            CaretAssert(0);
            break;
        case NeuroglancerAnnotationTypeEnum::AXIS_ALIGNED_BOUNDING_BOX:
            /* Two XYZ */
            offset *= 2;
            break;
        case NeuroglancerAnnotationTypeEnum::ELLIPSOID:
            /* Center XYZ + XYZ-Radius */
            offset *= 2;
            break;
        case NeuroglancerAnnotationTypeEnum::LINE:
            /* Two XYZ Endpoints */
            offset *= 2;
            break;
        case NeuroglancerAnnotationTypeEnum::POINT:
            /* One XYZ */
            offset *= 1;
            break;
        case NeuroglancerAnnotationTypeEnum::POLYLINE:
            offset *= 2;
            break;
    }
    
    for (const QJsonValue &pv : propsArr) {
        const QJsonObject po = pv.toObject();
        
        const AString id(po.value("id").toString());
        const AString description(po.value("description").toString());
        const AString typeName(po.value("type").toString().toLower());

        DataType dataType(DataType::INVALID);
        int32_t dataTypeSize(0);
        if (typeName == "rgb") {
            dataType = DataType::RGB;
            dataTypeSize = 3;
        }
        else if (typeName == "rgba") {
            dataType = DataType::RGBA;
            dataTypeSize = 4;
        }
        else if (typeName == "uint8") {
            dataType = DataType::UINT8;
            dataTypeSize = 1;
        }
        else if (typeName == "int8") {
            dataType = DataType::INT8;
            dataTypeSize = 1;
        }
        else if (typeName == "uint16") {
            dataType = DataType::UINT16;
            dataTypeSize = 2;
        }
        else if (typeName == "int16") {
            dataType = DataType::INT16;
            dataTypeSize = 2;
        }
        else if (typeName == "uint32") {
            dataType = DataType::UINT32;
            dataTypeSize = 4;
        }
        else if (typeName == "int32") {
            dataType = DataType::INT32;
            dataTypeSize = 4;
        }
        else if (typeName == "float32") {
            dataType = DataType::FLOAT32;
            dataTypeSize = 4;
        }
        else {
            throw DataFileException("Unknown type=" + typeName);
        }
        CaretAssert(dataType != DataType::INVALID);
        
        std::vector<AString> enumLabels;
        if (po.contains("enum_labels")) {
            for (const QJsonValue &lv : po.value("enum_labels").toArray())
                enumLabels.push_back(lv.toString());
        }
        
        std::vector<int32_t> enumValues;
        if (po.contains("enum_values")) {
            enumValues = readIntArray(po.value("enum_values").toArray());
        }
        
        Property property;
        property.m_propertyType = dataType;
        property.m_description = description;
        property.m_id = id;
        property.m_fileOffset = offset;

        if ( ! enumLabels.empty()) {
            if (enumLabels.size() == enumValues.size()) {
                const int32_t numItems(enumLabels.size());
                for (int32_t i = 0; i < numItems; i++) {
                    property.m_enumValueLabel.insert(std::make_pair(enumValues[i],
                                                                    enumLabels[i]));
                }
            }
            else {
                throw DataFileException("Number of enum values="
                                        + AString::number(enumValues.size())
                                        + " and enum labels="
                                        + AString::number(enumLabels.size())
                                        + " do not match for property: "
                                        + description);
            }
        }

        m_properties.push_back(property);

        offset += dataTypeSize;
    }
}

std::vector<NeuroglancerAnnotationsFile::SpatialGrid>
NeuroglancerAnnotationsFile::readSpatial(const QJsonArray &spatialArr)
{
    std::vector<SpatialGrid> grids;
    grids.reserve(spatialArr.size());
    
    for (const QJsonValue &sv : spatialArr) {
        const QJsonObject so = sv.toObject();
        
        SpatialGrid g;
        g.m_path = so.value("key").toString();
        g.m_chunkSize = readIntArray(so.value("chunk_size").toArray());
        g.m_gridShape = readIntArray(so.value("grid_shape").toArray());
        g.m_limit = so.value("limit").toInt();
        
        grids.push_back(g);
    }
    return grids;
}

