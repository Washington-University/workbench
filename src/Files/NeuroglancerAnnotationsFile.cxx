/**
 * Read the data files in the 'byId" directory
 */
void
NeuroglancerAnnotationsFile::readNeuroglancerAnnotationFiles()
{
    QDir dir(m_byIdDirectoryName);
    if ( ! dir.exists()) {
        throw DataFileException("byID directory does not exist: "
                                + m_byIdDirectoryName);
    }
    
    const QStringList filenamesList(dir.entryList(QDir::Files));
    QStringListIterator iter(filenamesList);
    std::vector<AString> annFileNames(filenamesList.begin(),
                                      filenamesList.end());
    AStringNaturalComparison::sortStringVector(annFileNames);
    
    int64_t rgbOffset(-1);
    int64_t rgbaOffset(-1);
    for (const Property& p : m_properties) {
        if (p.m_id == "point_color") {
            if (p.m_propertyType == DataType::RGB) {
                rgbOffset = p.m_fileOffset;
            }
            else if (p.m_propertyType == DataType::RGBA) {
                rgbaOffset = p.m_fileOffset;
            }
            else {
                throw DataFileException("point_color is neither RGB nor RGBA but is "
                                        + dataTypeToString(p.m_propertyType));
            }
        }
    }
    
    float xScale(1.0);
    float yScale(1.0);
    float zScale(1.0);
    if (m_xDimension.m_valid) {
        xScale = m_xDimension.m_unitScale * m_xDimension.m_resolution;
        CaretAssert(xScale != 0.0);
    }
    if (m_yDimension.m_valid) {
        yScale = m_yDimension.m_unitScale * m_yDimension.m_resolution;
        CaretAssert(yScale != 0.0);
    }
    if (m_zDimension.m_valid) {
        zScale = m_zDimension.m_unitScale * m_zDimension.m_resolution;
        CaretAssert(zScale != 0.0);
    }

    for (const AString& filenameOnly : annFileNames) {
        const AString pathAndName(m_byIdDirectoryName
                                  + QDir::separator()
                                  + filenameOnly);
        QFile file(pathAndName);
        if ( ! file.open(QFile::ReadOnly)) {
            throw DataFileException("Failed to read file "
                                    + pathAndName);
        }
        
        /*
         * Neuorglancer files are LITTLE ENDIAN Single Precision
         */
        QDataStream dataStream(&file);
        dataStream.setByteOrder(QDataStream::LittleEndian);
        dataStream.setFloatingPointPrecision(QDataStream::SinglePrecision);
        
        float x,y,z;
        dataStream >> x >> y >> z;
        
//        x *= xScale;
//        y *= yScale;
//        z *= zScale;
//        
        uint8_t r(255), g(255), b(255), a(0);
        if (rgbOffset >= 0) {
            if (file.seek(rgbOffset)) {
                dataStream >> r >> g >> b;
            }
            else {
                throw DataFileException("Failed to seek to RGB offset");
            }
        }
        if (rgbaOffset >= 0) {
            if (file.seek(rgbaOffset)) {
                dataStream >> r >> g >> b >> a;
            }
            else {
                throw DataFileException("Failed to seek to RGBA offset");
            }
        }
        
        bool supportedFlag(false);
        switch (m_annotationType) {
            case NeuroglancerAnnotationTypeEnum::INVALID:
                break;
            case NeuroglancerAnnotationTypeEnum::AXIS_ALIGNED_BOUNDING_BOX:
                break;
            case NeuroglancerAnnotationTypeEnum::ELLIPSOID:
                break;
            case NeuroglancerAnnotationTypeEnum::LINE:
                break;
            case NeuroglancerAnnotationTypeEnum::POINT:
                supportedFlag = true;
                break;
            case NeuroglancerAnnotationTypeEnum::POLYLINE:
                break;
        }
        if ( ! supportedFlag) {
            throw DataFileException("Neuroglancer Annotation type not supported for "
                                    + NeuroglancerAnnotationTypeEnum::toGuiName(m_annotationType));
        }
        
        std::array<uint8_t, 4> rgba { r, g, b, a };
        CaretColor color;
        color.setCustomColorRGBA(rgba);
        color.setCaretColorEnum(CaretColorEnum::CUSTOM);
        
        std::vector<Vector3D> xyzs { Vector3D(x, y, z) };
        
        NeuroglancerAnnotation* na = new NeuroglancerAnnotation(m_annotationType,
                                                                filenameOnly,
                                                                xyzs,
                                                                color);
        m_annotations.emplace_back(na);
    }
}

/**
 * @return Number of annotations
 */
int32_t
NeuroglancerAnnotationsFile::getNumberOfAnnotations() const
{
    return m_annotations.size();
}

